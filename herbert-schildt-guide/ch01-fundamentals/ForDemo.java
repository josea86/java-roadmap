class ForDemo{
	public static void main(String[] args){

		int counter; // Declares the variable counter

		// Declares the for statement with counter = 0 and increasing the value of counter by 1 till 5

		for(counter = 0; counter <= 5; counter++){
			System.out.println("This is count " + counter);
		}

		// When the for ends, shows a message "Done!"
		
		System.out.println("Done!");
	}
}